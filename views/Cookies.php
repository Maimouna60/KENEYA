CHARTE RELATIVE AUX COOKIES
Lors de la consultation de notre site Internet, des informations relatives à la navigation de votre terminal sont susceptibles d’être enregistrées dans des fichiers appelés « Cookies », sous réserve des choix que vous auriez exprimés et que vous pouvez modifier à tout moment.
QU’EST-CE QU’UN COOKIE ?
Un cookie est un fichier texte déposé, sous réserve de votre choix, sur votre ordinateur, votre mobile ou votre tablette lors de la visite d’un site ou de la consultation d’une publicité.

Il a pour but de collecter des informations relatives à votre navigation et de vous proposer des services personnalisés.

Les cookies sont gérés par votre navigateur web et seul son émetteur est susceptible de lire ou de modifier les informations qui y sont contenues.

LES DIFFERENTS TYPES DE COOKIES
1. Les cookies internes
Il s’agit des cookies émis par notre site Internet.

Les cookies de session
Ils permettent de mémoriser les données d’identification des utilisateurs d’une page à l’autre afin qu’ils puissent parcourir de nombreuses pages d'un site rapidement et simplement sans avoir à s’identifier de nouveau ou à retraiter chaque nouvelle partie du site qu’ils visitent. Une fois votre session fermée, le cookie sera détruit.

Les cookies d’audience
Ces cookies permettent d’établir des statistiques, volume de fréquentation et d’utilisation des divers éléments composant notre site afin d’améliorer son ergonomie et de mieux comprendre l’audience du site.

Ces cookies sont anonymisés, et par conséquent, ils ne pourront servir à suivre la navigation de l’internaute sur d’autres sites.

2. Les cookies tiers
Notre site est susceptible de faire appel à des services tiers spécialisés pour enrichir les solutions de communication ou lui permettre de générer des revenus.

Ces cookies supplémentaires assureront par exemple l’affichage de services type : régie publicitaire, recommandation de contenus utiles, partage sur réseaux sociaux, commentaires sur publications, etc.

En pratique, il s’agit des cookies déposés sur votre terminal par des sociétés tierces lorsque vous naviguez ou cliquez dans les espaces publicitaires du site.

ACCEPTER OU REFUSER LES COOKIES
Lorsqu’un cookie est susceptible d’être déposé sur votre page Internet, un bandeau vous informe de sa présence.

Vous avez la possibilité de l’accepter en poursuivant votre navigation sur le site. Mais vous pouvez également refuser les cookies au cas par cas ou systématiquement. Nous vous rappelons que le paramétrage est susceptible de modifier vos conditions d'accès à nos services nécessitant l'utilisation de cookies.

Si votre navigateur est configuré de manière à refuser l'ensemble des cookies, vous ne pourrez pas profiter de certains de nos services. Afin de gérer les cookies au plus près de vos attentes nous vous invitons à paramétrer votre navigateur en tenant compte de la finalité des cookies telle que mentionnée ci-avant.

