<h2 class="grey-text mb-1"><strong>Qu’est-ce qu’un cookie ?</strong></h2>
<p>Quand l’Utilisateur entre dans un Service, ce dernier envoie automatiquement un cookie à son ordinateur. Il s’agit de petits dossiers contenant du texte, qui permettent à un serveur de reconnaître un ordinateur et donc de savoir si l’ordinateur (et probablement l’Utilisateur) a déjà visité le Service précédemment. 
Si l’ordinateur se reconnecte au Service, ce dernier cherchera et se servira de ces cookies, ou “témoins” laissés sur le disque dur pendant la précédente visite.</p>
<p>Les cookies eux–mêmes ne permettent pas d’identifier un Utilisateur, mais uniquement l’ordinateur qu’il utilise. Les cookies se bornent à prendre note des parties du Service que l’ordinateur en question a visité, et du temps qu’il y a passé.</p>
<h2 class="grey-text mb-1"><strong>Usage des cookies par le Servie</strong></h2>
Pendant toute visite sur le Service, les pages vues par l’Utilisateur ainsi que des “cookies” sont téléchargés par l’ordinateur de l’Utilisateur.

<p>L’Utilisateur est libre de s’opposer à l’enregistrement de “cookies” en configurant son navigateur via ses paramètres ; cependant en refusant les cookies, 
il sera impossible de lui fournir certains services personnalisés et cette manipulation risque de ralentir ou d’empêcher l’accès à certaines fonctionnalités du Service.</p>
